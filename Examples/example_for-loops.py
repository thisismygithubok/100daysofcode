fruits = ["Apple","Peach","Pear"]

for fruit in fruits: #Declares each item in the fruits list is "fruit"
    print(fruit) #Prints each item from the list
    print(fruit+" Pie") #Prints each item + the word pie
print(fruits) #Prints at the end, meaning not in each iteration of the for loop